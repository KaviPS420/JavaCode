public class AsciiValues {

	public static void main(String[] args) {
		for(char ch='a';ch<='z';ch++) {
			System.out.println(ch+0);
		}

		for(char ch1='A';ch1<='Z';ch1++) {
			System.out.println(ch1+0);
		}

		for(char ch2='0';ch2<='9';ch2++) {
			System.out.println(ch2+0);
		}
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
		
	}

}
